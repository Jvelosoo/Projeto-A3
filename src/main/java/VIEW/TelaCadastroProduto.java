    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        String nome = txtNome.getText();
        double preco = Double.parseDouble(txtQuantidade.getText());
        int quantidade = Integer.parseInt(txtQuantidade.getText());
        String categoria = cmbCategoria.getSelectedItem().toString();

        DTOProdutos produto = new DTOProdutos(nome, preco, quantidade, categoria);
        DAO dao = new DAO(ConexaoBD.obtemConexao());

        try {
            dao.inserirProduto(produto);
            JOptionPane.showMessageDialog(null, "Produto inserido com sucesso.");
            txtNome.setText("");
            txtQuantidade.setText("");
            txtQuantidade.setText("");
            cmbCategoria.setSelectedIndex(0);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro ao inserir o produto: " + e.getMessage());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Erro de formato de número: " + e.getMessage());
        } catch (Exception ex) {
            Logger.getLogger(TelaCadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnInserirActionPerformed

