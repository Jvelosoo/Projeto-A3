    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // pega o login e a senha
        String login = loginTextField.getText();
        String senha = new String(senhaPasswordField.getPassword());

        // Verificação sem a base de dados
        /*if(login.equals("admin") && senha.equals("admin")){
           JOptionPane.showMessageDialog(null,"Bem Vindo!");
       }else{
           JOptionPane.showMessageDialog(null,"Usuário Inválido!!");
       }*/
        // Verificação com a base de dados
        try {
            // Criar um objeto da classe Usuario
            DTOUsuario usuario = new DTOUsuario(login, senha);
            DAO dao = new DAO(ConexaoBD.obtemConexao());

            //if(dao.existe(usuario) == true)
            if (dao.existe(usuario)) {
                JOptionPane.showMessageDialog(null, "Bem vindo, " + usuario.getLogin() + "!");

            } else {
                JOptionPane.showMessageDialog(null, "Usuário inválido!!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Problemas Técnicos. Tente Novamente.");
        }
    }//GEN-LAST:event_loginButtonActionPerformed

